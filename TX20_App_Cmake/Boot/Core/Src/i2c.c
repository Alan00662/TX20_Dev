{
	LL_I2C_HandleTransfer(I2Cx, addr, LL_I2C_ADDRSLAVE_7BIT, 2, LL_I2C_MODE_AUTOEND, LL_I2C_GENERATE_START_WRITE);
	
	while(!LL_I2C_IsActiveFlag_TXIS(I2Cx))
    { 
    }	
	LL_I2C_TransmitData8(I2Cx,reg);

	while(!LL_I2C_IsActiveFlag_TXIS(I2Cx))
    { 
    }	
	LL_I2C_TransmitData8(I2Cx,data);

}

